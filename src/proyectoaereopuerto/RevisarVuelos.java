    private void cargarModelo() {

        try {
            String[] titulos = {"Numero de vuelo", "Destino vuelo", "Fecha de salida", "Modelo del avi√≥n", "Estado del avion", "Id del Avion", "Nombre del Piloto"};
            String[] registros = new String[7];
            modelo = new DefaultTableModel(null, titulos);
            conexionUsuarios cc = new conexionUsuarios();
            Connection cn = cc.conectar();
            String sql = "";
            sql = "SELECT V.ID_VUEL, V.DEST_VUEL, V.FEC_SAL_VUEL, A.MOD_AVI, A.EST_AVI, A.ID_AVI, A.ID_PILOT_AVI FROM vuelos V , aviones A";
            Statement psd = cn.createStatement();
            ResultSet rs = psd.executeQuery(sql);
            while (rs.next()) {
                registros[0] = rs.getString("ID_VUEL");
                registros[1] = rs.getString("DEST_VUEL");
                registros[2] = rs.getString("FEC_SAL_VUEL");
                registros[3] = rs.getString("MOD_AVI");
                registros[4] = rs.getString("EST_AVI");
                registros[5] = rs.getString("ID_AVI");
                registros[6] = rs.getString("ID_PILOT_AVI");
                modelo.addRow(registros);
            }

            jtbl_Registro.setModel(modelo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    
    }

